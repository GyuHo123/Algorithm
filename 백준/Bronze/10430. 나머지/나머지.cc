#include<iostream>

using namespace std;

int main()
{
    int A,B,C;
    
    cin >> A >> B >> C;
    
    while(1)
        if(2<= A,B,C <=10000)
            break;
        else
            continue;
   
    cout << (A+B)%C << endl;
    cout << ((A%C) + (B%C))%C << endl;
    cout << (A*B)%C << endl;
    cout << ((A%C) * (B%C))%C << endl;
        
}